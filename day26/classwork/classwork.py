def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"
    

# 2
def number_to_string(num):
    return str(num)

# 3
def make_negative( number ):
    if number <= 0:
        return number
    else:
        return number * (-1)
    

# 4
def bool_to_word(boolean):
    if boolean:
        return "Yes"
    else:
        return "No"
    
